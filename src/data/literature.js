// ── 工具函数 ──

export function getAllAuthors() {
  return [...new Set(LITERATURE_QUOTES.map(q => q.author))]
}

export function getAllSeasons() {
  return [...new Set(LITERATURE_QUOTES.map(q => q.season))]
}

export function getAllFoodTypes() {
  return [...new Set(LITERATURE_QUOTES.map(q => q.food))]
}

/** 根据月份获取季节名称 */
export function getSeasonByMonth(month) {
  if (month >= 3 && month <= 5) return '春'
  if (month >= 6 && month <= 8) return '夏'
  if (month >= 9 && month <= 11) return '秋'
  return '冬'
}

/** 按季节筛选 */
export function getQuotesBySeason(season) {
  if (!season || season === '通用') return LITERATURE_QUOTES
  return LITERATURE_QUOTES.filter(q => q.season === season || q.season === '通用')
}

/** 按食物类型筛选 */
export function getQuotesByFood(food) {
  if (!food || food === '通用') return LITERATURE_QUOTES
  return LITERATURE_QUOTES.filter(q => q.food === food || q.food === '通用')
}

/** 按季节 + 食物类型筛选 */
export function getQuotesBySeasonAndFood(season, food) {
  if (!season && !food) return LITERATURE_QUOTES
  return LITERATURE_QUOTES.filter(q =>
    (!season || q.season === season || q.season === '通用') &&
    (!food || q.food === food || q.food === '通用')
  )
}

/** 按作者筛选 */
export function getQuotesByAuthor(author) {
  return LITERATURE_QUOTES.filter(q => q.author === author)
}

/** 随机获取 N 条 */
export function getRandomQuotes(count = 1) {
  const shuffled = [...LITERATURE_QUOTES].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count)
}

export function getRandomQuote() {
  return LITERATURE_QUOTES[Math.floor(Math.random() * LITERATURE_QUOTES.length)]
}

